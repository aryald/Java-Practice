package datatypespractice;

public class SwitchPractice2 {

	public static void main(String[] args) {
		int a = 20;
		switch (a) {
		case 10+10:
			System.out.println("Print Devraj");
			break;
		case 30-30:
			System.out.println("Print Anu");
			break;
		case 40*40:
			System.out.println("print Bibek");
			break;
		case 50/10:
			System.out.println("Print Rishika");
			break;
		default:
			System.out.println("Print Invalid name");
		}

	}
}
